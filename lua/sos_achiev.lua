        AddEventSlideX(achievWindowGain2.ID, 0, 2, nil);
        AddSingleUseTimer(4, 'hideAchievmentWindow(' .. achievWindowGain2.ID .. ')');
    else
        setVisible(achievWindowGain3, true);
        setTexture(achievWindowGain3.Image, 'SGUI/Achiev/' .. NAME .. '.png');
